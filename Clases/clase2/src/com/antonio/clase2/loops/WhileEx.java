public class WhileEx {
	
	public static void main(String[] args) {
		/*
		 * bloque while ejecuta un bloque de codigo
		 * siempre que su condicion sea verdadera
		 * 
		 * 
		 * while(condicion){
		 *     -----
		 *     codigo a ejecutar;
		 *     -----
		 * }
		 * 
		 */
		
		
		
		int i = 0;
		while(i <= 10) {
			System.out.println(i);
			i++;
		}
		
		
		
		
		
		
		
		
	}

}
